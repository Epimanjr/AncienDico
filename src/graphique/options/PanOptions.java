    /**
     * Activation du contraste
     */
    public final void activerContraste() {
        /* Modification PANEL */
        this.setBackground(Donnees.contraste.couleurs.get("fond"));

        // Label titre
        jLabel1.setBackground(Donnees.contraste.couleurs.get("fond"));
        jLabel1.setForeground(Donnees.contraste.couleurs.get("labelTitre"));

        // Labels
        jLabel2.setBackground(Donnees.contraste.couleurs.get("fond"));
        jLabel2.setForeground(Donnees.contraste.couleurs.get("label"));
        jLabel3.setBackground(Donnees.contraste.couleurs.get("fond"));
        jLabel3.setForeground(Donnees.contraste.couleurs.get("label"));
        jLabel4.setBackground(Donnees.contraste.couleurs.get("fond"));
        jLabel4.setForeground(Donnees.contraste.couleurs.get("label"));

        // Modification bouton
        boutonSauve.setBackground(Donnees.contraste.couleurs.get("boutonFond"));
        boutonSauve.setForeground(Donnees.contraste.couleurs.get("bouton"));
        choixDossier.setBackground(Donnees.contraste.couleurs.get("boutonFond"));
        choixDossier.setForeground(Donnees.contraste.couleurs.get("bouton"));
    }

    /**
     * Initialisation des données du panel
     */
    private void initialiserDonneesPanel() {
        // Les CheckBox
        checkVerbeux.setSelected(Donnees.isModeVerbeux());
        checkVoice.setSelected(Donnees.isVoice());
    }

    private void sauvegarderDonnees() {
        // Les CheckBox
        String modeVerbeux = (checkVerbeux.isSelected()) ? "oui" : "non";
        Options.addValue("modeverbeux", modeVerbeux);
        String voice = (checkVoice.isSelected()) ? "oui" : "non";
        Options.addValue("voice", voice);

        // Sauvegarde

    }
