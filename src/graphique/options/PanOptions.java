    private void choixDossierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choixDossierActionPerformed
        // TODO add your handling code here:

        JFileChooser jfc = new JFileChooser();
        jfc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        String monFichier;
        String approve = "Choisir";
        int resultatEnregistrer = jfc.showDialog(jfc, approve);
        if (resultatEnregistrer == JFileChooser.APPROVE_OPTION) {

            // Dossier
            Options.addValue("dossier", jfc.getSelectedFile().toString());

        }
    }//GEN-LAST:event_choixDossierActionPerformed


